
public class Player {
	private int money;
	private int morale;
	private int luck;
	private boolean knowsEnglish;
	private boolean isMarried;
	private int numberOfChildren;
	private Job job;
	private Education education;
	private String[] friends;
	private int score;
	private House house;
	private Swedish swedishLevel;
	public Player(int money, int morale, int luck, boolean knowsEnglish,
			boolean isMarried, int numberOfChildren, Job job,
			Education education, String[] friends, int score, House house,
			Swedish swedishLevel) {
		super();
		this.money = money;
		this.morale = morale;
		this.luck = luck;
		this.knowsEnglish = knowsEnglish;
		this.isMarried = isMarried;
		this.numberOfChildren = numberOfChildren;
		this.job = job;
		this.education = education;
		this.friends = friends;
		this.score = score;
		this.house = house;
		this.swedishLevel = swedishLevel;
	}
	
	
	

}
